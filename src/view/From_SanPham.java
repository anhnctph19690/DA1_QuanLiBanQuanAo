    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        QLSanPham sp = this.getfromdate();
        if (sp == null) {
            return;
        }
        this.sanPhamService.insert(sp);
        JOptionPane.showMessageDialog(this, "thanh cong");
        Loadtable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:int row = tbKhachhang.getSelectedRow();
       int row = tbSanPham.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui long chon");
            return;

        }
        QLSanPham sp = this.getfromdate();
        this.sanPhamService.update(sp, sp.getIdSanPham());
        JOptionPane.showMessageDialog(this, "thanh cong");
        Loadtable();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tbSanPham.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui long chon");
            return;

        }
        QLSanPham sp = this.getfromdate();
        this.sanPhamService.delete(sp.getIdSanPham());
        JOptionPane.showMessageDialog(this, "thanh cong");
        Loadtable();
    }//GEN-LAST:event_btnXoaActionPerformed

