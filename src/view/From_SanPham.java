public void Loadtable(){
    DefaultTableModel dmt = (DefaultTableModel) tbSanPham.getModel();
        dmt.setRowCount(0);

        for (QLSanPham sp : this.sanPhamService.getAll()) {
            Object[] row = {
                sp.getIdSanPham(),
                sp.getMaSP(),
                sp.getTenSP()
            };
            dmt.addRow(row);
        }
}
